package javaprog.work.lec01;

class Sample03 {

	public static void main(String[] args) {
		long a = 15L;
		long b = 4L;

		long ans1 = a + b;
		long ans2 = a - b;
		long ans3 = a * b;
		double ans4 = (double) a / 4;

		System.out.println("a＋b＝" + ans1);
		System.out.println("a−b＝" + ans2);
		System.out.println("a×b＝" + ans3);
		System.out.println("a÷b＝" + ans4);
	}

}
